    private void doctorIdTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_doctorIdTextActionPerformed
        //Auto fill doctor details to DoctorEntryForm
        Doctor doctor;
        try {
            doctor = DoctorController.searchDoctorDetail(doctorIdText.getText());
            if (doctor != null) {
                //DoctorEntryForm.doctorIdText.setText(doctorIdText.getText());
                DoctorEntryForm.employeeIdText.setText(Integer.toString(doctor.getEmployee_Employee_id()));
                DoctorEntryForm.qualificationsText.setText(doctor.getQualifications());
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DetailsGetDoctorId.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(DetailsGetDoctorId.class.getName()).log(Level.SEVERE, null, ex);
        }
        

        Employee employee = null;
        try {
            employee = EmployeeController.searchEmployeeDetail(employeeIdText.getText());
            if (employee != null) {
                DoctorEntryForm.employeeIdText.setText(employeeIdText.getText());
                DoctorEntryForm.firstNameText.setText(employee.getFirst_name());
                DoctorEntryForm.lastNameText.setText(employee.getLast_name());
                DoctorEntryForm.nicText.setText(employee.getNIC());
                DoctorEntryForm.addressText.setText(employee.getAddress());
                DoctorEntryForm.sexCombo.setSelectedItem(employee.getSex());
                DoctorEntryForm.contactNumberText.setText(employee.getContact_no());
                DoctorEntryForm.registrationDateText.setText(employee.getRegistration_date());
                DoctorEntryForm.emailText.setText(employee.getEmail());
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DetailsGetEmployeeId.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(DetailsGetEmployeeId.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_doctorIdTextActionPerformed

