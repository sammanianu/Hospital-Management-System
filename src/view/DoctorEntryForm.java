    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String Employee_id_String = employeeIdText.getText();
        int Employee_id = Integer.parseInt(Employee_id_String); 
        String Doctor_id_String = doctorIdText.getText();
        int Doctor_id = Integer.parseInt(Doctor_id_String); 
        String NIC = nicText.getText();
        String First_name = firstNameText.getText();
        String Last_name = lastNameText.getText();
        String Sex = (String)sexCombo.getSelectedItem();
        String Email = emailText.getText();
        String Registration_date = registrationDateText.getText();
        String Address = addressText.getText();
        String Qualifications = qualificationsText.getText();
        String Contact_no = contactNumberText.getText();
        
        Doctor doctor = new Doctor(Doctor_id,Qualifications,Employee_id, First_name, Last_name, NIC, Address, Sex, Contact_no, Registration_date, Email);
        
        try {
            boolean updateEmployee=EmployeeController.updateEmployee(doctor);
            boolean updateDoctor = DoctorController.UpdateDoctor(doctor);
            if(updateDoctor){
            JOptionPane.showMessageDialog(this, "Successful");
            }else{
            JOptionPane.showMessageDialog(this, "Failed");
            }
            if(updateEmployee){
            JOptionPane.showMessageDialog(this, "Su");
            }else{
            JOptionPane.showMessageDialog(this, "Fa");
            }
        } catch (SQLException ex) {
            Logger.getLogger(DoctorEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(DoctorEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed


