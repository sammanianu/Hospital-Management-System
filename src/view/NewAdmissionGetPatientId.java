    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed
        String patientIdString = patientIdText.getText();
        int patient_id = Integer.parseInt(patientIdString);
        
        
        AdmissionEntryForm aef = new AdmissionEntryForm();
        aef.setVisible(true);
        NewAdmissionGetPatientIdPanel.setLayout(new BorderLayout());
        NewAdmissionGetPatientIdPanel.removeAll();
        NewAdmissionGetPatientIdPanel.add(aef);
        NewAdmissionGetPatientIdPanel.validate();
        NewAdmissionGetPatientIdPanel.repaint();
        
        //Auto fill admission id AdmissionEntryForm
        try {
            int admissionId = AdmissionController.getLastAdmissionId();
           
            String id = new Integer(admissionId+1).toString();
            aef.admissionIdText.setText(id);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Add.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Add.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        //Auto fill patient id to AdmissionEntryForm
        aef.patientIdText.setText(patientIdString);
        
        //Auto fill other details of admission Entry form
        try {
            Patient_record patientRecord = PatientController.searchPatientDetail(patientIdText.getText());
            if (patientRecord != null) {
            firstNameText.setText(patientRecord.getFirst_name());
            lastNameText.setText(patientRecord.getLast_name());
            nicText.setText(patientRecord.getNIC());
            genderCombo.setSelectedItem(patientRecord.getSex());//not swork
            addressText.setText(patientRecord.getAddress());
            ageText.setText(Integer.toString(patientRecord.getAge()));
            bloodGroupCombo.setSelectedItem(patientRecord.getBlood_group());//not work
            contactNoText.setText(Integer.toString(patientRecord.getContact_no()));
            sicknessHistoryText.setText(patientRecord.getSickness_history());
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewAdmissionGetPatientId.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(NewAdmissionGetPatientId.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_nextButtonActionPerformed


