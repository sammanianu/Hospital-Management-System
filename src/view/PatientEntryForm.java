    private void nextButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_nextButtonActionPerformed

        String First_name = firstNameText.getText();
        String Last_name = lastNameText.getText();
        String NIC = nicText.getText();
        String Sex = (String) genderCombo.getSelectedItem();
        String Address = addressText.getText();
        String AgeString = ageText.getText();
        int Age = Integer.parseInt(AgeString);
        String Blood_group = (String) bloodGroupCombo.getSelectedItem();
        String contactNoString = contactNumberText.getText();
        int Contact_no = Integer.parseInt(contactNoString);
        String Sickness_history = sicknessHistoryText.getText();

        Patient_record patient = new Patient_record(First_name, Last_name, NIC, Address, Sex, Age, Blood_group, Contact_no, Sickness_history);
        //Ward ward = new Ward(Ward_type);
        //Receptionist receptionist = 
        try {
            boolean addPatient = PatientController.addPatient(patient);
            if (addPatient) {
                JOptionPane.showMessageDialog(this, "Successful");
            } else {
                JOptionPane.showMessageDialog(this, "Failed");
            }
        } catch (SQLException ex) {
            Logger.getLogger(PatientEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PatientEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        }

        //add AdmissionEntryFormPanel to PatientEntryFormPanel    
        AdmissionEntryForm aef = new AdmissionEntryForm();
        aef.setVisible(true);
        patientEntryFormPanel.setLayout(new BorderLayout());
        patientEntryFormPanel.removeAll();
        patientEntryFormPanel.add(aef);
        patientEntryFormPanel.validate();
        patientEntryFormPanel.repaint();

        //Auto fill admission id to Admission Entry Form
        int admissionId;
        try {
            admissionId = AdmissionController.getLastAdmissionId();
            String aid = new Integer(admissionId + 1).toString();
            aef.admissionIdText.setText(aid);

        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PatientEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PatientEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        }

        //Auto fill patient id to Admission Entry Form
        try {
            int patientId = PatientController.getLastPatientId();
            String pid = new Integer(patientId).toString();
            aef.patientIdText.setText(pid);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(PatientEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(PatientEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        }

        //Auto fill other details in admission entry form
        String firstName = firstNameText.getText();
        aef.firstNameText.setText(firstName);

        String lastName = lastNameText.getText();
        aef.lastNameText.setText(lastName);

        String nic = nicText.getText();
        aef.nicText.setText(nic);

        String sex = (String) genderCombo.getSelectedItem();
        AdmissionEntryForm.genderCombo.setSelectedItem(sex);

        String address = addressText.getText();
        aef.addressText.setText(address);

        String ageString = ageText.getText();
        int age = Integer.parseInt(ageString);
        aef.ageText.setText(AgeString);

        String bloodGroup = (String) bloodGroupCombo.getSelectedItem();
        AdmissionEntryForm.genderCombo.setSelectedItem(bloodGroup);

        String ContactNoString = contactNumberText.getText();
        int contactNo = Integer.parseInt(ContactNoString);
        aef.contactNoText.setText(contactNoString);

        String sicknessHistory = sicknessHistoryText.getText();
        aef.sicknessHistoryText.setText(sicknessHistory);
    }//GEN-LAST:event_nextButtonActionPerformed

