    private void patientIdTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_patientIdTextActionPerformed
        
        try {
            Patient_record patientRecord = PatientController.searchPatientDetail(patientIdText.getText());
            if (patientRecord != null) {
            //PatientEntryForm.patientIdLabel.setText(patientIdText.getText());
            PatientEntryForm.firstNameText.setText(patientRecord.getFirst_name());
            PatientEntryForm.lastNameText.setText(patientRecord.getLast_name());
            PatientEntryForm.nicText.setText(patientRecord.getNIC());
            PatientEntryForm.addressText.setText(patientRecord.getAddress());
            PatientEntryForm.genderCombo.setSelectedItem(patientRecord.getSex());
            PatientEntryForm.ageText.setText(Integer.toString(patientRecord.getAge()));
            PatientEntryForm.bloodGroupCombo.setSelectedItem(patientRecord.getBlood_group());
            PatientEntryForm.contactNumberText.setText(Integer.toString(patientRecord.getContact_no()));
            PatientEntryForm.sicknessHistoryText.setText(patientRecord.getSickness_history());
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(NewAdmissionGetPatientId.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(NewAdmissionGetPatientId.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_patientIdTextActionPerformed

