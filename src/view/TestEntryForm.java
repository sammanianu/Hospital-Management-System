    private void submitButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submitButtonActionPerformed
        String AdmissionIdString = admissionIdText.getText();
        int Admission_id = Integer.parseInt(AdmissionIdString);
        String DoctorIdString = doctorIdText.getText();
        int Doctor_id = Integer.parseInt(DoctorIdString);
        String Category = testCategoryText.getText();
        String Description = trdText.getText();
        String Date = dateText.getText();
        String Result = (String)resultsCombo.getSelectedItem();
        
        int Test_id = 0;
        try {
            Test_id = TestController.getLastTestId()+1;
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TestEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TestEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        Test test = new Test(Doctor_id,Category,Description,Date,Result);
        AdmissionHasTest ahs = new AdmissionHasTest(Admission_id,Test_id);
        
        try {
            boolean addTest = TestController.addTest(test);
            boolean addAHT = AdmissionHasTestController.addAdmissionHasTest(ahs);
            if(addTest){
                JOptionPane.showMessageDialog(this, "Successful");
            }else{
                JOptionPane.showMessageDialog(this, "Failed");
            }
        } catch (SQLException ex) {
            Logger.getLogger(TestEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TestEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_submitButtonActionPerformed

