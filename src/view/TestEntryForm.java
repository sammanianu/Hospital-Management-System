    private void admissionIdTextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_admissionIdTextActionPerformed
        //Auto fill test id
        try {
            AdmissionHasTest admissionHasTest = AdmissionHasTestController.searchAdmissionId(admissionIdText.getText());
            if (admissionHasTest!=null){
                TestEntryForm.testIdText.setText(Integer.toString(admissionHasTest.getTest_Test_id()));
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TestEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TestEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        }
        
         //auto fill other test details
        try {
            Test test = TestController.searchTestDetail(testIdText.getText());
            if (test!=null){
                TestEntryForm.doctorIdText.setText(Integer.toString(test.getDoctor_id()));
                TestEntryForm.testCategoryText.setText(test.getCategory());
                TestEntryForm.trdText.setText(test.getDescription());
                TestEntryForm.dateText.setText(test.getDate());
                TestEntryForm.resultsCombo.setSelectedItem(test.getCategory());
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(TestEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(TestEntryForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_admissionIdTextActionPerformed


